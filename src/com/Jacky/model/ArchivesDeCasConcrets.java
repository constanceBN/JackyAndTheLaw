package com.Jacky.model;

public class ArchivesDeCasConcrets {

	private int idArchives;
	private String sujetArchives;
	private String contenuArchives;

	public int getIdArchives() {
		return idArchives;
	}

	public void setIdArchives(int idArchives) {
		this.idArchives = idArchives;
	}

	public String getSujetArchives() {
		return sujetArchives;
	}

	public void setSujetArchives(String sujetArchives) {
		this.sujetArchives = sujetArchives;
	}

	public String getContenuArchives() {
		return contenuArchives;
	}

	public void setContenuArchives(String contenuArchives) {
		this.contenuArchives = contenuArchives;
	}

}
