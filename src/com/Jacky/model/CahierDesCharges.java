package com.Jacky.model;

public class CahierDesCharges {

	private int idCahierDesCharges;
	private String typeDeLicences;
	private String domaineProfessionnel;
	private double honorairesJuriste;
	private int exempleCasConcrets;  
	private int idJuriste;

	public int getIdCahierDesCharges() {
		return idCahierDesCharges;
	}

	public void setIdCahierDesCharges(int idCahierDesCharges) {
		this.idCahierDesCharges = idCahierDesCharges;
	}

	public String getTypeDeLicences() {
		return typeDeLicences;
	}

	public void setTypeDeLicences(String typeDeLicences) {
		this.typeDeLicences = typeDeLicences;
	}

	public String getDomaineProfessionnel() {
		return domaineProfessionnel;
	}

	public void setDomaineProfessionnel(String domaineProfessionnel) {
		this.domaineProfessionnel = domaineProfessionnel;
	}

	public double getHonorairesJuriste() {
		return honorairesJuriste;
	}

	public void setHonorairesJuriste(double honorairesJuriste) {
		this.honorairesJuriste = honorairesJuriste;
	}

	public int getExempleCasConcrets() {
		return exempleCasConcrets;
	}

	public void setExempleCasConcrets(int exempleCasConcrets) {
		this.exempleCasConcrets = exempleCasConcrets;
	}

	public int getIdJuriste() {
		return idJuriste;
	}

	public void setIdJuriste(int idJuriste) {
		this.idJuriste = idJuriste;
	}

}
