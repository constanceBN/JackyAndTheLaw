package com.Jacky.model;

public class Juriste {

	private int idJuriste;
	private String nomJuriste;
	private String prenomJuriste;
	private int appreciation;

	public int getIdJuriste() {  
		return idJuriste;
	}

	public void setIdJuriste(int idJuriste) {
		this.idJuriste = idJuriste;
	}

	public String getNomJuriste() {
		return nomJuriste;
	}

	public void setNomJuriste(String nomJuriste) {
		this.nomJuriste = nomJuriste;
	}

	public String getPrenomJuriste() {
		return prenomJuriste;
	}

	public void setPrenomJuriste(String prenomJuriste) {
		this.prenomJuriste = prenomJuriste;
	}

	public int getAppreciation() {
		return appreciation;
	}

	public void setAppreciation(int appreciation) {
		this.appreciation = appreciation;
	}

}
